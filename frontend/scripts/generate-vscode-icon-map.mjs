const availableFiles = new Set(
  fs.readdirSync(packageDir).filter((file) => file.endsWith(".js")),
);

const files = (useFullIconMap
  ? [...availableFiles]
  : commonIconModuleNames
      .map((moduleName) => `${moduleName}.js`)
      .filter((file) => availableFiles.has(file))
).sort((a, b) => a.localeCompare(b));

const lines = [
  "// Auto-generated by scripts/generate-vscode-icon-map.mjs",
  "// Do not edit manually.",
  useFullIconMap
    ? "// Generated with VSCODE_ICON_MAP=full."
    : "// Generated with the compact icon set. Use VSCODE_ICON_MAP=full for every VS Code icon.",
  "",
  "export const vscodeIconLoaders = {",
];

for (const file of files) {
  const moduleName = file.replace(/\.js$/i, "");
  lines.push(
    `    \"${moduleName}\": () => import(\"@iconify-icons/vscode-icons/${file}\"),`,
  );
}

lines.push("};", "");

fs.mkdirSync(path.dirname(outputFile), { recursive: true });
fs.writeFileSync(outputFile, lines.join("\n"));
